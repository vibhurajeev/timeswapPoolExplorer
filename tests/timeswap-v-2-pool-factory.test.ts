import {
  assert,
  describe,
  test,
  clearStore,
  beforeAll,
  afterAll
} from "matchstick-as/assembly/index"
import { Address } from "@graphprotocol/graph-ts"
import { AcceptOwner } from "../generated/schema"
import { AcceptOwner as AcceptOwnerEvent } from "../generated/TimeswapV2PoolFactory/TimeswapV2PoolFactory"
import { handleAcceptOwner } from "../src/timeswap-v-2-pool-factory"
import { createAcceptOwnerEvent } from "./timeswap-v-2-pool-factory-utils"

// Tests structure (matchstick-as >=0.5.0)
// https://thegraph.com/docs/en/developer/matchstick/#tests-structure-0-5-0

describe("Describe entity assertions", () => {
  beforeAll(() => {
    let owner = Address.fromString("0x0000000000000000000000000000000000000001")
    let newAcceptOwnerEvent = createAcceptOwnerEvent(owner)
    handleAcceptOwner(newAcceptOwnerEvent)
  })

  afterAll(() => {
    clearStore()
  })

  // For more test scenarios, see:
  // https://thegraph.com/docs/en/developer/matchstick/#write-a-unit-test

  test("AcceptOwner created and stored", () => {
    assert.entityCount("AcceptOwner", 1)

    // 0xa16081f360e3847006db660bae1c6d1b2e17ec2a is the default address used in newMockEvent() function
    assert.fieldEquals(
      "AcceptOwner",
      "0xa16081f360e3847006db660bae1c6d1b2e17ec2a-1",
      "owner",
      "0x0000000000000000000000000000000000000001"
    )

    // More assert options:
    // https://thegraph.com/docs/en/developer/matchstick/#asserts
  })
})
